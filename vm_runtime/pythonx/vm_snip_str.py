# [ get_args : 将一个以‘，’作为分割符的字符串转换为列表 ] {{{
def get_args(arglist):
    args = [arg.strip() for arg in arglist.split(',') if arg]
    return args
# }}} ! [ get_args : 将一个以‘，’作为分割符的字符串转换为列表 ]
# ------------------------------------------------------------------------------------------------ #

# ------------------------------------------------------------------------------------------------ #
# [ split_file ] {{{
def split_file(filename):
    fileparts = [part.strip() for part in filename.split('/') if part]
    if len(fileparts)>0:
      return fileparts[-1]
    else:
      return ""
# }}} ! [ split_file ]
# ------------------------------------------------------------------------------------------------ #

# ------------------------------------------------------------------------------------------------ #
# [ get_template_names : 将template name 转换为string ] {{{
def get_template_names( sTxt ):
    args = get_args( sTxt )
    lsArgName = "";
    if args:
      for index in range(len(args)):
        if index>0:
          lsArgName += ","
        lsArgName += args[index].split(" ",-1)[-1]
    return lsArgName
# }}} ! [ get_template_names : 将template name 转换为string ]
